package rs.math.oop1.p040303.literali.z01.tipovi;

public class Literali {

   public static void main(String[] args) {
      // celobrojni literali
      byte ceoByte = 34;
      int ceoInt = -100000;
      // postoje jos 3 celobrojna tipa podataka: byte, short i long

      // realni literali
      float realniFloat = 34.35f;
      double realniDouble = 35.565;

      // logicki literali
      boolean logickiTacno = true;

      // znakovni literali
      char znak1 = 'A';
      char znak2 = '\n';

      // stringovni literali
      String string = "Ovo je stringovni literal.";
   }
}
